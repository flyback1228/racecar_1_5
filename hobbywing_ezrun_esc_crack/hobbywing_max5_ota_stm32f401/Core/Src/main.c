HAL_StatusTypeDef read_ble_data(uint8_t* data, ESC_SensorTypeDef* esc_sensor){
	static uint8_t start_index = 0;
	for(uint8_t i=start_index;i<start_index+SENSOR_DATA_SIZE;++i){
		if(data[i]==0xFE && data[(i+1)%SENSOR_DATA_SIZE]==0x01){
			start_index = i%SENSOR_DATA_SIZE;
			break;
		}
	}

	if(data[(2+start_index)%SENSOR_DATA_SIZE]!=0x00)return HAL_ERROR;
	if(data[(3+start_index)%SENSOR_DATA_SIZE]!=0x03)return HAL_ERROR;
	if(data[(4+start_index)%SENSOR_DATA_SIZE]!=0x30)return HAL_ERROR;
	if(data[(5+start_index)%SENSOR_DATA_SIZE]!=0x5c)return HAL_ERROR;
	if(data[(6+start_index)%SENSOR_DATA_SIZE]!=0x17)return HAL_ERROR;
	if(data[(7+start_index)%SENSOR_DATA_SIZE]!=0x06)return HAL_ERROR;

	esc_sensor->throttle = (int16_t)(data[(9+start_index)%SENSOR_DATA_SIZE]);
	if(data[(11+start_index)%SENSOR_DATA_SIZE]!=0x01)esc_sensor->throttle=-esc_sensor->throttle;

	esc_sensor->rpm = ((uint32_t)(data[(14+start_index)%SENSOR_DATA_SIZE] <<8) | (data[(13+start_index)%SENSOR_DATA_SIZE]))*10;
	esc_sensor->voltage = (float)(data[(15+start_index)%SENSOR_DATA_SIZE])/10.0f;
	esc_sensor->current = ((uint16_t)(data[(16+start_index)%SENSOR_DATA_SIZE] <<8) | (data[(17+start_index)%SENSOR_DATA_SIZE]))/10.0;
	esc_sensor->temperature = (uint16_t)(data[(18+start_index)%SENSOR_DATA_SIZE] <<8) | (data[(19+start_index)%SENSOR_DATA_SIZE]);

	return HAL_OK;
}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
	if(huart->Instance==USART2){
		if(read_ble_data(ble_receive, &esc_sensor_data)==HAL_OK){
			printf("Throttle: %d\n",esc_sensor_data.throttle);
			printf("rpm: %lu\n",esc_sensor_data.rpm);
			printf("Voltage: %0.1f\n",esc_sensor_data.voltage);
			printf("Current: %0.1f\n",esc_sensor_data.current);
			printf("Temperature: %u\n",esc_sensor_data.temperature);
		}
	}
}

